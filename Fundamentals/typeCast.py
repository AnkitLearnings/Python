a_int = 30
print(a_int)
print(type(a_int))
# int to float
a_float = float(a_int)
print(a_float)
print(type(a_float))

b_str = "35"
print(b_str)
print(type(b_str))
# str to int
b_int= int(b_str)
print(b_int)
print(type(b_int))

c_float = 3.14
print(c_float)
print(type(c_float))
# float to int
c_int = int(c_float)
print(c_int)
print(type(c_int))

d_str = "True"
print(d_str)
print(type(d_str))
# str to boolean
d_bool = bool(d_str)
